public class variable4 {
    public static void main(String[] args) {
        // Deklarasi dan inisialisasi variabel numerik dengan nilai 15
        int numerik = 15;  // numerik awal 15

        // Mengubah nilai numerik menjadi 20
        numerik = 20;      // numerik sekarang 20

        // Membersihkan layar (mungkin, tergantung implementasi)
        System.out.println("\f");

        // Mencetak nilai akhir dari variabel numerik
        System.out.println("Maka nilai yang tampil sekarang: " + numerik);
    }
}