public class variable6 {
    public static void main(String[] args) {
        // Mendeklarasikan variabel untuk menyimpan bagian-bagian nama
        String namaDepan = "Muhammad ";
        String namaTengah = "Yusuf ";
        String namaBelakang = "Ibadurrohman";

        // Menggabungkan bagian-bagian nama menjadi nama lengkap
        String namaLengkap = namaDepan + namaTengah + namaBelakang;

        // Membersihkan layar (mungkin, tergantung implementasi)
        System.out.println("\f");

        // Mencetak nama lengkap
        System.out.println(namaLengkap);
    }
}